"""
news_sentiment_scorer.py
========================
Converts recent rows from `news_articles` into properly-scored
`sentiment_events` rows that the trading runtime will accept as
PRIMARY sentiment confirmation (i.e. not the inferred-only default
events generated by the safety sentiment_periodic loop).

Why this is needed:
  trading_runtime.module_degradation_reason("sentiment") flags the
  module as degraded if >= 70 % of the last-24h sentiment_events are
    weightedScore == 0.5  AND  raw.llm_analysis missing/empty.
  The free sentiment_periodic loop emits exactly that pattern (because
  it intentionally avoids Emergent LLM credits). So with only that
  source running, Sentiment can never go "active" even though we have
  hundreds of RSS articles.

This scorer:
  1. Reads recent news_articles per symbol.
  2. Scores them with VADER (lightweight, offline, no API).
  3. Writes one sentiment_events doc per article with:
        weightedScore != 0.5   (actual VADER compound mapped to 0..1)
        source = "news_rss_vader"
        raw.llm_analysis = {…} non-empty (counts as primary confirmation)
        symbol = the canonical symbol it talks about

It is idempotent (article_id is the unique key) and safe to re-run.
"""

from __future__ import annotations

import asyncio
import hashlib
import logging
import os
import re
import sys
from datetime import datetime, timedelta, timezone

from dotenv import load_dotenv

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
load_dotenv(os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), ".env"))

from motor.motor_asyncio import AsyncIOMotorClient
from vaderSentiment.vaderSentiment import SentimentIntensityAnalyzer

logging.basicConfig(level=logging.INFO, format="%(asctime)s [%(name)s] %(message)s")
